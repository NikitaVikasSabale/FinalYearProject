            <p>Forest(Conservation) ACT, 1980 with Amendments Made in 1988<br>
                An Act provide for the conservation of forests and for matters connected there with or ancillary or incidental there to.<br>
                Be it enacted by Parliament in the Thirty-first Year of the Republic of India as follows:-</p>
                <ol>
                <li><strong>Short title, extent and commencement :-</strong>
                <ol class="list-style order-lower-roman">
                <li class="pd-top15">This Act may be called the Forest (Conservation) Act, 1980.</li>
                <li>It extends to the whole of India except the State of Jammu &amp; Kashmir.</li>
                <li>It shall be deemed to have come into force on the 25th day of October, 1980.</li>
                </ol>
                </li>
                <li><strong>Restriction on the dereservation of forests or use of forest land for non-forest purpose :-</strong>
                <p class="pd-top15">Notwithstanding anything contained in any other law for the time being in force in a State, no State Government or other authority shall make, except with the prior approval of the Central Government, any order directing-</p>
                <ul>
                <li>That any reserved forest (within the meaning of the expression " reserved forest" in any law for the time being in force in that State) or any portion there of, shall cease to be reserved;</li>
                <li>That any forest land or any portion thereof may be used for any non-forest purpose;</li>
                <li>That any forest land or any portion thereof may be assigned by way of lease or otherwise to any private person or to any authority, corporation, agency or any other organization not owned, managed or controlled by Government;</li>
                <li>That any forest land or any portion thereof may be cleared of trees which have grown naturally in that land or portion, for the purpose of using it for re afforestation.</li>
                </ul>
                <h2 class="heading4">Explanation:-</h2>
                <p>For the purpose of this section, " non-forest purpose" means the breaking up or clearing of any forest land or portion thereof for</p>
                <ul>
                <li>The cultivation of tea, coffee, spices, rubber, palms, oil-bearing plants, horticulture crops or medicinal plants;</li>
                <li>any purpose other than reafforestation,</li>
                </ul>
                <p>but does not include any work relating or ancillary to conservation, development and management of forests and wildlife, namely, the establishment of check-posts, fire lines, wireless communications and construction of fencing, bridges and culverts, dams, waterholes, trench marks, boundary marks, pipelines or other like purposes.</p>
                </li>
                <li><strong>Constitution of Advisory Committee :-</strong>
                <p>The Central Government may constitute a Committee consisting of such number of persons as it may deem fit to advise that Government with regard to :-</p>
                <ul>
                <li>The grant of approval under Section 2; and</li>
                <li>Any other matter connected with the conservation of forests which may be referred to it by Central Government.</li>
                </ul>
                <p><strong>3A. Penalty for contravention of the provisions of the Act :-</strong></p>
                <p>Whoever contravenes or abets the contravention of any of the provisions of Section 2, shall be punishable with simple imprisonment for a period which may extend to fifteen days.</p>
                <p><strong>3B. Offences by authorities and Government Departments :-</strong></p>
                <ol>
                <li>Where any offence under this Act has been committed-
                <ol class="list-style order-lower-roman">
                <li>by any department of Government, the head of the department;or</li>
                <li>by any authority, every person who, at the time the offence was committed, was directly in charge of, and was responsible to, the authority for the conduct of the business of the authority as well as the authority;</li>
                </ol>
                <p>Shall be deemed to be guilty of the offence and shall be liable to be proceeded against and punished accordingly;</p>
                <p>Provided that nothing contained in this sub-section shall render the head of the department or any person referred to in clause (b), liable to any punishment if he proves that the offence was committed without his knowledge or that he exercised all due diligence to prevent the commission of such offence.</p>
                </li>
                <li>Notwithstanding anything contained in sub-section (1), where an offence punishable under the Act has been committed by a department of Government or any authority referred to in clause (b) of sub-section (1) and it is proved that the offence has been committed with the consent or connivance of; or is attributable to any neglect on the part of any officer, other than the head of the department, or in the case of authority, any person other than the persons referred to in clause (b) of sub-section (1), such officer or persons shall also be deemed to be guilty of that offence and shall be liable to be proceeded against and punished accordingly.</li>
                </ol>
                </li>
                <li><strong>Power to make rules:-</strong>
                <ol>
                <li>The Central Government may, by notification in the Official Gazette, make rules for carrying out the provisions of this Act.</li>
                <li>Every rule made under this Act shall be laid, as soon as may be after it is made, before each House of Parliament, while it is in session, for a total period of thirty days which may be comprised in one session or in two or more successive sessions, and if, before the expiry of the session immediately following the session or the successive sessions aforesaid, both Houses agree in making any modification in the rule or both Houses agree that the rule should not be made, the rule shall thereafter have effect only in such modified form or be of no effect, as the case may be, so, however, that any such modification or annulment shall be without prejudice to the validity of anything previously done under that rule.</li>
                </ol>
                </li>
                <li><strong>Repeal and Saving:-</strong>
                <ol>
                <li>The Forest (Conservation) Ordinance, 1980 is hereby replaced.</li>
                <li>Notwithstanding such repeal, anything done or any action taken under the provisions of the said Ordinance shall be deemed to have been done or taken under the corresponding provisions of this Act.</li>
                </ol>
                </li>
                </ol>
